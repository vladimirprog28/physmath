window.level_1 = 
function level_1(){
    level_number = 1;

    fetch('./phys.json')
        .then(Response => Response.json())
        .then( data => {
            data.level_number.n = level_number;

            localStorage.setItem('physData', JSON.stringify(data));
            console.log("успешно");
            console.log(data.level_number.n);
        })
    window.location.replace("physgame.html")
}
